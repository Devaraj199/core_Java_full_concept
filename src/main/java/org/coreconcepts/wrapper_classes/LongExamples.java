package org.coreconcepts.wrapper_classes;

public class LongExamples {
    public static void main(String[] args) {
        System.out.println(Long.valueOf("56"));
        System.out.println(Long.valueOf(56));
        System.out.println(Long.max(23,89));
        System.out.println(Long.min(23,89));
        System.out.println(Long.sum(23,89));
        System.out.println(Long.parseLong("89"));
        System.out.println(Long.toString(344344444));
        System.out.println(Long.compare(23,4));
    }
}
