package org.coreconcepts.wrapper_classes;

public class IntegerClassExample {
    public static void main(String[] args) {
        System.out.println(Integer.max(2,4));
        System.out.println(Integer.min(2,4));
        System.out.println(Integer.parseInt("63773"));
        System.out.println(Integer.sum(23,3));
        System.out.println(Integer.valueOf(23));
        System.out.println(Integer.toString(244));
        System.out.println(Integer.reverse(45));
    }
}
